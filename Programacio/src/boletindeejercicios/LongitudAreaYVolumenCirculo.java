package boletindeejercicios;

import java.util.Scanner;

public class LongitudAreaYVolumenCirculo {

	/*
	 * Programa que toma como dato de entrada un número que corresponde a la
	 * longitud de un radio y nos escribe la longitud de la circunferencia, el área
	 * del círculo y el volumen de la esfera que corresponden con dichoradio
	 * 
	 */
	public static void main(String[] args) {
		Scanner teclado = new Scanner(System.in);

		// Pedir la longitud de el radio

		System.out.println("Introduce la longitud de el radio");
		int radio = teclado.nextInt();

		// Calculos

		double longitud = 2 * 3.14 * radio;
		double area = 3.14 * (radio * radio);
		double volumen = (4 / 3) * 3.14 * (radio * radio * radio);

		// Mostrar resultados

		System.out.println("La longitud de la circunferencia es : " + longitud);
		System.out.println("El área de la circunferencia es: " + area);
		System.out.println("El volumen de la circunferencia es: " + volumen);

		teclado.close();

	}

}

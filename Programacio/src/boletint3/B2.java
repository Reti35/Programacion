package boletint3;

public class B2 {

	/*
	 * Programa que muestra los números pares comprendidos entre el 1 y el 200. Para
	 * ello utiliza un contador y suma de 2 en 2.
	 */

	public static void main(String[] args) {

		for (int contador = 0; contador <= 200; contador = contador + 2) {

			System.out.println(contador);

		}

		System.out.println("Fin");

	}

}
